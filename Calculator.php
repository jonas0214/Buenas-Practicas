<?php  

class Calculator
{
    public function add(int$a, int $b){
        
        return $a + $b;
    
    }
    
    public function resta(int$a, int $b){
        
        return $a - $b;
    
    }

    public function multiplicacion(int$a, int $b){
        
        return $a * $b;
    
    }

    public function division(int$a, int $b){
        
        return $a / $b;
    
    }
}